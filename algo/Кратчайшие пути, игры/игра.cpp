using uint = std::uint32_t;
using size_type = std::uint32_t;
using weight_t = std::int64_t;

struct edge
{
	size_type t;
	weight_t w;

	edge() = default;

	edge(size_type t, weight_t w) : t(t), w(w) {}
};

int main(void)
{
	std::ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	size_type n, m, s;
	cin >> n >> m >> s;
	s--;

	std::vector<std::vector<size_type>> graph(n);

	for (size_type i = 0; i < m; i++)
	{
		size_type u, v;
		cin >> u >> v;
		u--;
		v--;
		graph[u].emplace_back(v);
	}

	std::vector<char> memoize(n, 0);

	std::function<bool(size_type s)> dfs = [&](size_type u) {
		if (graph[u].empty())
			return true;
		if (memoize[u])
			return !!(memoize[u] - 1);
		for (auto const& v : graph[u])
			if (dfs(v))
			{
				memoize[u] = 1;
				return false;
			}
		memoize[u] = 2;
		return true;
	};

	cout << (dfs(s) ? "Second" : "First") << " player wins" << std::endl;

	return 0;
}