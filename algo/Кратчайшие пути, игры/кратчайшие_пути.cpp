using uint = std::uint32_t;
using size_type = std::uint16_t;
using weight_t = std::int64_t;

struct edge
{
	size_type t;
	weight_t w;

	edge() = default;
	
	edge(size_type t, weight_t w) : t(t), w(w) {}
};

int main(void)
{
	std::ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	size_type n, m, s;
	cin >> n >> m >> s;
	s--;

	std::vector<std::vector<edge>> graph(n);
	for (size_type i = 0; i < m; i++)
	{
		size_type f, t;
		weight_t w;
		cin >> f >> t >> w;
		f--;
		t--;
		graph[f].emplace_back(t, w);
	}

	constexpr weight_t maxw = std::numeric_limits<weight_t>::max() - 2'000'000'000'000'000LL;

	std::vector<weight_t> d(n, maxw);

	d[s] = 0;

	for (size_type _ = 0; _ < n + 5; _++)
	{
		for (size_type v = 0; v < n; v++)
		{
			if (d[v] == maxw)
				continue;
			for (auto const& e : graph[v])
				d[e.t] = std::min(d[e.t], d[v] + e.w);
		}
	}

	std::vector<bool> isbad(n, false);

	std::function<void(size_type)> dfs_mark = [&](size_type v) {
		if (d[v] == maxw || isbad[v])
			return;
		isbad[v] = true;
		for (auto const& e : graph[v])
			dfs_mark(e.t);
	};

	std::vector<bool> visited(n, false);

	std::function<void(size_type)> dfs_check = [&](size_type v) {
		if (d[v] == maxw || visited[v] || isbad[v])
			return;
		visited[v] = true;
		for (auto const& e : graph[v])
			if (d[e.t] > d[v] + e.w)
				dfs_mark(e.t);
			else
				dfs_check(e.t);
	};

	dfs_check(s);

	for (size_type v = 0; v < n; v++)
		if (isbad[v])
			cout << "-\n";
		else if (d[v] == maxw)
			cout << "*\n";
		else
			cout << d[v] << '\n';

	return 0;
}