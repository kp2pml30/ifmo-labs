using uint = std::uint32_t;
using size_type = std::uint32_t;
using weight_t = std::int64_t;

void DoTest(void)
{
	size_type n, m;
	cin >> n >> m;

	if (cin.eof())
		return;

	std::vector<std::vector<size_type>> graph(n), reversed(n);
	std::vector<size_type> count(n, 0);

	std::vector<size_type> queue;
	queue.reserve(n);

	for (size_type i = 0; i < m; i++)
	{
		size_type u, v;
		cin >> u >> v;
		u--;
		v--;
		graph[u].emplace_back(v);
		reversed[v].emplace_back(u);
	}

	std::vector<char> memoize(n, 0);

	for (size_type i = 0; i < n; i++)
		if (graph[i].empty())
		{
			queue.emplace_back(i);
			memoize[i] = -1;
		}

	while (!queue.empty())
	{
		auto v = queue.back();
		queue.pop_back();
		if (memoize[v] == -1)
		{
			for (auto const& u : reversed[v])
				if (memoize[u] == 0)
				{
					memoize[u] = 1;
					queue.push_back(u);
				}
		}
		else
			for (auto const& u : reversed[v])
				if (++count[u] == graph[u].size())
				{
					memoize[u] = -1;
					queue.push_back(u);
				}
	}
	char
		const* mapper_base[3] = {"SECOND", "DRAW", "FIRST"},
		*const* selector = mapper_base + 1;

	for (int i = 0; i < n; i++)
		cout << selector[memoize[i]] << '\n';
}

int main(void)
{
	std::ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	while (!cin.eof())
	{
		DoTest();
		cout << std::endl;
	}

	return 0;
}