using uint = std::uint32_t;
using size_type = std::uint16_t;
using weight_t = std::int64_t;

struct edge
{
	weight_t w;
	size_type t;

	edge() = default;

	edge(size_type t, weight_t w) : t(t), w(w) {}
};

int main(void)
{
	std::ios_base::sync_with_stdio(false);
	cin.tie(nullptr);

	size_type n;
	cin >> n;

	std::vector<std::vector<edge>> graph(n);

	for (size_type i = 0; i < n; i++)
		for (size_type j = 0; j < n; j++)
		{
			weight_t w;
			cin >> w;
			if (w != 100'000)
				graph[i].emplace_back(j, w);
		}

	std::vector<weight_t> d(n, 0);
	std::vector<size_type> p(n, -1);

	size_type relaxed = -1;

	for (size_type _ = 0; _ < n; _++)
	{
		relaxed = -1;
		for (size_type i = 0; i < n; i++)
			for (auto const& e : graph[i])
				if (d[e.t] > d[i] + e.w)
				{
					p[e.t] = i;
					d[e.t] = d[i] + e.w;
					relaxed = e.t;
				}
	}
	if (relaxed != (size_type)-1)
	{
		size_type what = relaxed;

		std::vector<size_type> res;
		res.reserve(n);

		std::set<size_type> visited;
		visited.emplace(what);
		res.push_back(what);
		while (true)
		{
			what = p[what];
			if (what == (size_type)-1)
				throw "up";
			if (!visited.emplace(what).second)
				break;
			res.push_back(what);
		}

		size_type start_from = 0;
		while (res[start_from] != what)
			start_from++;
		cout << "YES\n" << res.size() - start_from << '\n';

		for (size_type i = res.size() - 1; i > start_from; i--)
			cout << res[i] + 1 << ' ';
		cout << res[start_from] + 1;

		return 0;
	}
	cout << "NO\n";
	return 0;
}