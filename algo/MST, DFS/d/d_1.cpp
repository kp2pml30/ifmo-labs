using uint = std::uint32_t;
using size_type = std::int64_t;
using graph_t = std::vector<std::vector<std::pair<size_type, size_type>>>;

std::vector<size_type> GetBicon(const graph_t &graph)
{
	std::vector<size_type> ans(graph.size(), 0);

	size_type curtime = 0, curcolor = 1;

	std::vector<size_type> entert(graph.size());
	std::vector<size_type> metric(graph.size());
	std::vector<bool> visited(graph.size(), false);

	std::vector<size_type> stack;

	auto mark = [&](size_type cur) {
		size_type lst;
		while (stack.size() && (lst = stack.back()) != cur)
		{
			stack.pop_back();
			if (ans[lst] != 0)
				throw "down";
			ans[lst] = curcolor;
		}
		ans[cur] = curcolor++;
		if (stack.size())
			stack.pop_back();
	};

	std::function<void(size_type, size_type)> dfs = [&](size_type cur, size_type edgenum) {
		stack.push_back(cur);
		visited[cur] = true;
		entert[cur] = curtime;
		metric[cur] = curtime++;
		bool dd = false;
		for (const auto &to : graph[cur])
		{
			if (to.second == edgenum)
				continue;
			if (visited[to.first])
				metric[cur] = std::min(metric[cur], entert[to.first]);
			else
			{
				dfs(to.first, to.second);
				metric[cur] = std::min(metric[cur], metric[to.first]);
				if (metric[to.first] > entert[cur])
					mark(to.first);
			}
		}
	};
	for (size_type i = 1; i < graph.size(); i++)
		if (!visited[i])
		{
			dfs(i, -1);
			mark(i);
			if (!stack.empty())
				throw "to hell";
		}
	return ans;
}

