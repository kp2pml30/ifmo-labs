	size_type n, m;
	cin >> n >> m;
	graph_t graph(n + 1);

	for (size_type i = 0; i < m; i++)
	{
		size_type a, b;
		cin >> a >> b;
		graph[a].emplace_back(b, i);
		graph[b].emplace_back(a, i);
	}

	auto ans = GetBicon(graph);
	std::cout << *std::max_element(ans.begin() + 1, ans.end()) << std::endl;
	std::unordered_map<size_type, size_type> mapped;
	size_type next = 0;
	for (int i = 1; i < ans.size(); i++)
	{
		if (ans[i] == 0)
			throw "away";
		const auto &a = ans[i];
		auto &bind = mapped[a];
		if (bind == 0)
			bind = ++next;
		std::cout << bind << ' ';
	}
	std::cout << std::endl;
	return 0;
