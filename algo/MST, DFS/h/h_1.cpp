using uint = std::uint32_t;
using size_type = std::uint16_t;
using weight_t = std::int32_t;
using graph_t = std::vector<std::vector<size_type>>;

struct edge
{
	size_type t;
	weight_t w;
	edge() = default;
	edge(size_type t,	weight_t w) : t(t), w(w) {}
};

int main(void)
{
  size_type n;
  cin >> n;

	std::vector<std::vector<edge>> graph(n), reversed(n);

	for (size_type i = 0; i < n; i++)
		for (size_type j = 0; j < n; j++)
		{
			weight_t c;
			cin >> c;
			if (i == j)
				continue;
			graph[i].emplace_back(j, c);
			reversed[j].emplace_back(i, c);
		}

	auto comparator = [](auto const& a, auto const& b) { return a.w < b.w; };

	for (auto& a : graph)
		std::sort(a.begin(), a.end(), comparator);
	for (auto& a : reversed)
		std::sort(a.begin(), a.end(), comparator);

	std::vector<bool> visited;
	int l = -1, r = 1'000'000'000;
	while (l + 1 < r)
	{
		int m = (l + r) / 2;
		size_type vn;

		{
			visited.assign(n, 0);
			visited[0] = true;
			vn = 1;
			std::function<void(size_type)> dfs = [&](size_type cur) {
				for (int ie = 0; ie < n - 1 && graph[cur][ie].w <= m; ie++)
				{
					if (!visited[graph[cur][ie].t])
					{
						visited[graph[cur][ie].t] = true;
						vn++;
						dfs(graph[cur][ie].t);
					}
				}
			};
			dfs(0);
		}
		if (vn != n)
		{
			l = m;
			continue;
		}

		{
			visited.assign(n, 0);
			visited[0] = true;
			vn = 1;
			std::function<void(size_type)> dfs = [&](size_type cur) {
				for (int ie = 0; ie < n - 1 && reversed[cur][ie].w <= m; ie++)
				{
					if (!visited[reversed[cur][ie].t])
					{
						visited[reversed[cur][ie].t] = true;
						vn++;
						dfs(reversed[cur][ie].t);
					}
				}
			};
			dfs(0);
		}
		if (vn != n)
		{
			l = m;
			continue;
		}

		r = m;
	}

	cout << r << std::endl;

  return 0;
}