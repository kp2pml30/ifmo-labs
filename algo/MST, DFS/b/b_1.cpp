std::vector<std::pair<size_type, size_type>> GetBridges(const graph_t &graph)
{
	std::vector<std::pair<size_type, size_type>> ans;

	size_type curtime = 0;

	std::vector<size_type> entert(graph.size());
	std::vector<size_type> metric(graph.size());
	std::vector<bool> visited(graph.size(), false);

	std::function<void(size_type, size_type)> dfs = [&](size_type cur, size_type from) {
		visited[cur] = true;
		entert[cur] = curtime;
		metric[cur] = curtime++;
		for (const auto &to : graph[cur])
		{
			if (to == from)
				continue;
			if (visited[to])
				metric[cur] = std::min(metric[cur], entert[to]);
			else
			{
				dfs(to, cur);
				metric[cur] = std::min(metric[cur], metric[to]);
				if (metric[to] > entert[cur])
					ans.emplace_back(cur, to);
			}
		}
	};
	for (size_type i = 1; i < graph.size(); i++)
		if (!visited[i])
			dfs(i, -1);
	return ans;
}

int main(void)
{
	constexpr bool directed = false;
  size_type n, m;
  cin >> n >> m;
	graph_t graph(n + 1);

	std::map<std::pair<size_type, size_type>, size_type> edge2num;

	for (size_type i = 0; i < m; i++)
	{
		size_type a, b;
		cin >> a >> b;
		graph[a].push_back(b);
		if constexpr (!directed)
			graph[b].push_back(a);
		edge2num.emplace(make_ordered_pair(a, b), i + 1);
	}

	auto ans = GetBridges(graph);
	std::cout << ans.size() << std::endl;
	std::vector<size_type> i_want_to_die;
	i_want_to_die.reserve(ans.size());
	for (const auto &a : ans)
		i_want_to_die.push_back(edge2num[make_ordered_pair(a.first, a.second)]);
	std::sort(i_want_to_die.begin(), i_want_to_die.end());
	for (const auto &a : i_want_to_die)
		std::cout << a << ' ';
	std::cout << std::endl;
  return 0;
}