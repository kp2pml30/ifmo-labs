using uint = std::uint32_t;
using size_type = std::uint32_t;
using graph_t = std::vector<std::vector<std::pair<size_type, size_type>>>;

int main(void)
{
	size_type n, m;
	cin >> n >> m;
	graph_t graph(n + 1), reversed(n + 1);
	for (size_type i = 0; i < m; i++)
	{
		size_type a, b;
		cin >> a >> b;
		graph[a].emplace_back(b, i);
		reversed[b].emplace_back(a, i);
	}

	std::vector<size_type> order;
	order.reserve(reversed.size());
	{
		std::vector<bool> visited(reversed.size());
		size_type t = 0;
		std::function<void(size_type)> dfs = [&](size_type cur) {
			if (visited[cur])
				return;
			visited[cur] = true;
			for (const auto &e : reversed[cur])
				dfs(e.first);
			// timeout[cur] = t++;
			order.push_back(cur);
		};
		for (size_type i = 1; i < graph.size(); i++)
			dfs(i);
	}

	std::vector<size_type> color(graph.size(), 0);
	size_type curcol = 1;
	{
		std::function<void(size_type)> dfs = [&](size_type cur) {
			if (color[cur] != 0)
				return;
			color[cur] = curcol;
			for (const auto &e : graph[cur])
				dfs(e.first);
		};
		std::reverse(order.begin(), order.end());
		for (const auto &i : order)
		{
			dfs(i);
			curcol++;
		}
	}
	std::set<std::pair<size_type, size_type>> edg;
	for (size_type i = 1; i < graph.size(); i++)
		for (const auto &e : graph[i])
			if (color[i] != color[e.first])
				edg.emplace(color[i], color[e.first]);
	cout << edg.size() << std::endl;
	return 0;
}