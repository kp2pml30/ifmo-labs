using uint = std::uint32_t;

using size_type = std::uint32_t;

int main(void)
{
  size_type n, m;
  cin >> n >> m;

	std::vector<std::vector<size_type>> graph(n + 1);

	for (size_type i = 0; i < m; i++)
	{
		size_type a, b;
		cin >> a >> b;
		graph[a].push_back(b);
		// graph[b].push_back(a);
	}
	{
		std::vector<char> visited(n + 1, 0);
		std::function<bool(size_type)> dfs = [&](size_type c) -> bool {
			if (visited[c])
				return visited[c] % 2; // 1 -> true, 2 -> false
			visited[c] = 1;
			for (const auto &i : graph[c])
			{
				if (dfs(i))
					return true;
			}
			visited[c] = 2;
			return false;
		};
		for (size_type i = 1; i < graph.size(); i++)
			if (dfs(i))
			{
				cout << -1 << std::endl;
				return 0;
			}
	}
	std::vector<size_type> ans;
	{
		std::vector<bool> visited(n + 1, false);
		std::function<void(size_type)> dfs = [&](size_type c) {
			if (visited[c])
				return;
			visited[c] = true;
			for (const auto &i : graph[c])
				dfs(i);
			ans.emplace_back(c);
		};
		for (size_type i = 1; i < graph.size(); i++)
			dfs(i);
	}
	for (auto i = ans.rbegin(); i != ans.rend(); ++i)
		cout << *i << ' ';
	cout << std::endl;
  return 0;
}