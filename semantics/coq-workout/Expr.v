(* Set of variables is an expression *)
Inductive V : expr -> id -> Prop := 
  v_Var : forall (id : id), id ? (Var id)
| v_Bop : forall (id : id) (a b : expr) (op : bop), id ? a \/ id ? b -> id ? (Bop op a b)
where "x ? e" := (V e x).

(* If an expression is defined in some state, then each its' variable is
   defined in that state
 *)
Lemma defined_expression
      (e : expr) (s : state Z) (z : Z) (id : id)
      (RED : [| e |] s => z)
      (ID  : id ? e) :
  exists z', s / id => z'.
Proof.
  induction RED.
  1, 2: now inv ID; eauto.
  all: now
    inv ID;
    inv H3;
    [apply (IHRED1 H)|apply (IHRED2 H)].
Qed.

(* If a variable in expression is undefined in some state, then the expression
   is undefined is that state as well
*)
Lemma undefined_variable (e : expr) (s : state Z) (id : id)
      (ID : id ? e) (UNDEF : forall (z : Z), ~ (s / id => z)) :
  forall (z : Z), ~ ([| e |] s => z).
Proof.
  intros w falso.
  destruct (defined_expression e s _ _ falso ID) as (zf, ef).
  apply (UNDEF zf ef).
Qed.

(* The evaluation relation is deterministic *)
Lemma eval_deterministic (e : expr) (s : state Z) (z1 z2 : Z) 
      (E1 : [| e |] s => z1) (E2 : [| e |] s => z2) :
  z1 = z2.
Proof.
  generalize dependent z1.
  generalize dependent z2.
  induction e; ins; inv E1; inv E2.
  1, 2: try now eapply state_deterministic; eauto.
  all:
    specialize (IHe1 _ VALA _ VALA0); subst;
    specialize (IHe2 _ VALB _ VALB0); subst;
    congruence.
Qed.

(* Equivalence of states w.r.t. an identifier *)
Definition equivalent_states (s1 s2 : state Z) (id : id) :=
  forall z :Z, s1 /id => z <-> s2 / id => z.

Lemma variable_relevance (e : expr) (s1 s2 : state Z) (z : Z)
      (FV : forall (id : id) (ID : id ? e),
          equivalent_states s1 s2 id)
      (EV : [| e |] s1 => z) :
  [| e |] s2 => z.
Proof.
  generalize dependent z.
  induction e; ins.
  { inv EV.
    apply SmokeTest.nat_always. }
  { constructor.
    inv EV.
    assert (i ? (Var i)) as ivari. { constructor. }
    destruct (FV i ivari z).
    auto.
  }
  assert
    (  (forall id: id, id ? e1 -> id ? (Bop b e1 e2))
    /\ (forall id: id, id ? e2 -> id ? (Bop b e1 e2))) as (l2b, r2b).
  { split; ins; constructor; auto. }
  assert (forall id: id, id ? (Bop b e1 e2) -> id ? e1 \/ id ? e2) as b2o.
  { intros id H. inv H. }
  assert (forall id: id, id ? e1 \/ id ? e2 -> equivalent_states s1 s2 id) as eqivstate.
  { intros id ro.
    destruct ro.
    all: assert (id ? (Bop b e1 e2)) as idInBop; eauto. }
  assert (forall id: id, id ? e1 -> equivalent_states s1 s2 id) as eqsl.
  { ins. eauto. }
  assert (forall id: id, id ? e2 -> equivalent_states s1 s2 id) as eqsr.
  { ins. eauto. }
  inv EV.
  all:
    specialize (IHe1 eqsl _ VALA);
    specialize (IHe2 eqsr _ VALB);
    econstructor; eauto.
Qed.

Definition equivalent (e1 e2 : expr) : Prop :=
  forall (n : Z) (s : state Z), 
    [| e1 |] s => n <-> [| e2 |] s => n.
Notation "e1 '~e~' e2" := (equivalent e1 e2) (at level 42, no associativity).

Lemma eq_refl (e : expr): e ~e~ e.
Proof.
  ins.
Qed.

Lemma eq_symm (e1 e2 : expr) (EQ : e1 ~e~ e2): e2 ~e~ e1.
Proof.
  unfold "~e~" in *.
  ins.
  destruct (EQ n s).
  split; auto.
Qed.

Lemma eq_trans (e1 e2 e3 : expr) (EQ1 : e1 ~e~ e2) (EQ2 : e2 ~e~ e3):
  e1 ~e~ e3.
Proof.
  unfold "~e~" in *.
  ins.
  destruct (EQ1 n s).
  destruct (EQ2 n s).
  split; auto.
Qed.

Inductive Context : Type :=
| Hole : Context
| BopL : bop -> Context -> expr -> Context
| BopR : bop -> expr -> Context -> Context.

Fixpoint plug (C : Context) (e : expr) : expr := 
  match C with
  | Hole => e
  | BopL b C e1 => Bop b (plug C e) e1
  | BopR b e1 C => Bop b e1 (plug C e)
  end.  

Notation "C '<e~' e" := (plug C e) (at level 43, no associativity).

Definition contextual_equivalent (e1 e2 : expr) : Prop :=
  forall (C : Context), (C <e~ e1) ~e~ (C <e~ e2).
Notation "e1 '~ec~' e2" := (contextual_equivalent e1 e2)
                            (at level 42, no associativity).

Lemma eq_eq_ceq (e1 e2 : expr) :
  e1 ~e~ e2 <-> e1 ~ec~ e2.
Proof.
  split.
  { unfold "~e~", "~ec~".
    ins.
    induction C.
    { simpl. eapply eq_trans; eauto. apply eq_refl. }
    1, 2: simpl;
      unfold "~e~";
      unfold "~e~" in IHC;
      ins;
      split;
        intros pre;
        inv pre;
        eremember (IHC _ s) as OK;
        destruct OK;
        econstructor; eauto.
  }
  unfold "~ec~".
  ins.
  specialize (H Hole).
  simpl in H.
  auto.
Qed.
