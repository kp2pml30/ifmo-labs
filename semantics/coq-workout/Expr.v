Module SmokeTest.

  Lemma nat_always n (s : state Z) : [| Nat n |] s => n.
  Proof.
    constructor.
  Qed.
  
  Lemma double_and_sum (s : state Z) (e : expr) (z : Z)
        (HH : [| e [*] (Nat 2) |] s => z) :
    [| e [+] e |] s => z.
  Proof.
    inv HH.
    inv VALB.
    assert (Z.mul za 2%Z = Z.add za za) as mul2add. { lia. }
    rewrite -> mul2add in *.
    constructor; auto.
  Qed.

End SmokeTest.

